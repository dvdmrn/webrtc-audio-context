let matMult = function (m, v) {
  var out = Array(m.length).fill(0);
  
  // Note the r = row, c = col is backward to let the vector be a "row".
  
  for (var r=0; r < v.length; r++) {
    for (var c=0; c < m.length; c++){
      out[c] += m[c][r] * v[r];
    }
  }
  
  return out;
}

